package operatorPractice;

public class UrinaryOperator {
	
	public static void main(String[] args) {
		
		int price1=38;
		int price2=40;
		
		++price1;
		System.out.println("pre increment is:"+price1);
		price1++;
		System.out.println("post increment is:"+price1);
		--price2;
		System.out.println("pre Decrement is:"+price2);
		price2--;
		System.out.println("post Decrement is:"+price2);
	}
	
	
	

}
