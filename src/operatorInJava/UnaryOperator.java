package operatorInJava;

public class UnaryOperator {
	
	
	
	public static void main(String[] args) {
		int a=5;
		++a;
		System.out.println(a);
		a++;
		System.out.println(a);
		int b=40;
		b++;
		System.out.println(b);
		++b;
		System.out.println(b);
		
		double x=7.5;
		++x;
		System.out.println(x);
		x++;
		System.out.println(x);
		--x;
		System.out.println(x);
		x--;
		System.out.println(x);
	}

}
